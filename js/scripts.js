//Basic math operator functions
function add (a,b){
    let sum = a+b;
    return sum;
}

add(2,3);

function subtract (a,b){
    let subt = a-b;
    return subt;
}

subtract(3,2);

function multiply(a,b){
    let mult = a*b;
    return mult;
}

multiply(2,3);

function division (a,b){
    let divide = a/b;
    return divide;    
}

division(6,2);